// ── CODE RAIN ──
(function(){
  const canvas = document.getElementById('codeRain');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');

  const chars = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz';
  const vsColors = ['#569CD6','#4EC9B0','#DCDCAA','#9CDCFE','#CE9178','#B5CEA8','#C586C0','#4FC1FF'];
  const fontSize = 14;
  const colWidth = 22; // spacing between columns
  const rowHeight = 22; // spacing between rows

  function hexToRgb(hex) {
    const h = hex.replace('#','');
    return [parseInt(h.slice(0,2),16), parseInt(h.slice(2,4),16), parseInt(h.slice(4,6),16)];
  }

  let columns = [];

  function init() {
    canvas.width  = canvas.offsetWidth;
    canvas.height = canvas.offsetHeight;
    const cols = Math.floor(canvas.width / colWidth);
    columns = Array.from({length: cols}, () => ({
      drop:  Math.random() * -(canvas.height / rowHeight),
      speed: 0.5 + Math.random() * 0.5,
      color: vsColors[Math.floor(Math.random() * vsColors.length)],
      chars: Array.from({length: 20}, () => chars[Math.floor(Math.random() * chars.length)]),
      tick: 0,
    }));
  }
  init();
  window.addEventListener('resize', init);

  let lastTime = 0;
  function draw(ts) {
    requestAnimationFrame(draw);
    if (ts - lastTime < 50) return;
    lastTime = ts;

    ctx.fillStyle = 'rgba(10,10,15,0.13)';
    ctx.fillRect(0, 0, canvas.width, canvas.height);
    ctx.font = fontSize + 'px monospace';

    const trailLen = 12;
    columns.forEach((col, i) => {
      const x = i * colWidth;
      const y = col.drop * rowHeight;
      const [r,g,b] = hexToRgb(col.color);

      col.tick++;
      if (col.tick % 4 === 0)
        col.chars[Math.floor(Math.random() * col.chars.length)] = chars[Math.floor(Math.random() * chars.length)];

      for (let t = trailLen; t >= 0; t--) {
        const ty = y - t * rowHeight;
        if (ty < -rowHeight) continue;
        if (t === 0) {
          ctx.fillStyle = `rgba(255,255,255,0.92)`;
        } else if (t === 1) {
          ctx.fillStyle = `rgba(${r},${g},${b},0.9)`;
        } else {
          const a = (1 - t / trailLen) * 0.5;
          ctx.fillStyle = `rgba(${r},${g},${b},${a})`;
        }
        ctx.fillText(col.chars[t % col.chars.length], x, ty);
      }

      col.drop += col.speed;
      if (col.drop * rowHeight > canvas.height + trailLen * rowHeight) {
        col.drop = Math.random() * -20;
        col.speed = 0.15 + Math.random() * 0.25;
        col.color = vsColors[Math.floor(Math.random() * vsColors.length)];
      }
    });
  }
  requestAnimationFrame(draw);
})();

